nome = input('Qual é o seu nome? ')
print('Como vai você', nome, '?')
resposta = input()

if resposta == 'bem':
    print('Que ótimo!')
elif resposta == 'mais ou menos':
    print('Calma ! vai melhorar!')
elif resposta == 'medio':
    print('Calma, chegaremos lá!')
elif resposta == 'tudo bem':
    print('Que coisa boa!')
elif resposta == 'tudo otimo':
    print('Que maravilha!')
elif resposta == 'maravilhosamente bem':
    print('Sensacional!')
else:
    print('Que pena!')
